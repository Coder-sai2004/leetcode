class Solution:
    def countCommas(self, n: int) -> int:
        c=0
        if n<1000:
            return 0
        elif n>999 and n<1000000:
            c=n-999
        elif n>999999 and n<1000000000:
            c=(999999-999)+(n-999999)*2
        elif n>999999999 and n<1000000000000:
            c=(999999-999)+((999999999-999999)*2)+((n-999999999)*3)
        elif n>999999999999 and n<1000000000000000:
            c=(999999-999)+((999999999-999999)*2)+((999999999999-999999999)*3)+((n-999999999999)*4)
        else:
            c=(999999-999)+((999999999-999999)*2)+((999999999999-999999999)*3)+((n-999999999999)*4)+1
        return c